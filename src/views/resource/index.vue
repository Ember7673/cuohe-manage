<template>
  <div class="requirementList">
    <el-breadcrumb separator="/">
      <el-breadcrumb-item>资源列表</el-breadcrumb-item>
    </el-breadcrumb>
    <div class="search">
      <span>资源查找：</span>
      <el-input v-model="searchStr" placeholder="请输入项目名称/资源介绍"></el-input>
      <el-button type="primary" size="mini" @click="onSearch">查询</el-button>
    </div>
    <div class="listContent">
      <el-tabs v-model="activeName" @tab-click="handleClick">
        <el-tab-pane label="全部" name="1,2">
          <ListModule :list="requirementList" :size="requirementLen" :pageindex="pageindex" @pageChange="pageChange" @refresh="refresh"></ListModule>
        </el-tab-pane>
        <el-tab-pane label="未审核" name="1">
          <ListModule :list="requirementList" :size="requirementLen" :pageindex="pageindex" @pageChange="pageChange" @refresh="refresh"></ListModule>
        </el-tab-pane>
        <el-tab-pane label="已审核" name="2">
          <ListModule :list="requirementList" :size="requirementLen" :pageindex="pageindex" @pageChange="pageChange" @refresh="refresh"></ListModule>
        </el-tab-pane>
      </el-tabs>
    </div>
  </div>
</template>

<script>
import { resourceListMoudle } from '@/api/requirement';
import ListModule from '@/components/resourceList'
import { uuid } from '@/utils';
export default {
  components: {
    ListModule
  },
  data () {
    return {
      searchStr: '',
      requirementList: [],
      activeName: '1,2',
      curStatus: '1,2',
      isDelete: false,
      pageindex: 1,
      requirementLen: 0,
    }
  },
  async created () {
    this.requirementList = await this.getRequirementList('1,2', 1, 10);
  },
  methods: {
    getRequirementList (status, pageindex, pagesize, keyword = '', isDelete = false) {
      return new Promise((resolve, reject) => {
        resourceListMoudle({
          reqid: uuid(),
          status,
          delete: isDelete,
          keyword,
          pageindex,
          pagesize
        }).then(res => {
          const {
            status,
            data,
            message
          } = res.data;
          if (!status) {
            this.requirementLen = data.size;
            resolve(data.meets);
          } else {
            this.$message.error(message)
            reject();
          }
        })
      })
    },
    async pageChange (index) {
      this.pageindex = index;
      this.requirementList = await this.getRequirementList(this.curStatus, index, 10, this.searchStr, this.isDelete);
    },
    async handleClick (e) {
      this.curStatus = e.name;
      if (this.curStatus === '5') {
        this.isDelete = true;
        this.requirementList = await this.getRequirementList('1,2', 1, 10, this.searchStr, true);
      } else {
        this.isDelete = false;
        this.requirementList = await this.getRequirementList(this.curStatus, 1, 10, this.searchStr);
      }
    },
    async onSearch () {
      this.requirementList = await this.getRequirementList(this.curStatus, 1, 10, this.searchStr);
    },
    async refresh () {
      this.requirementList = await this.getRequirementList(this.curStatus, this.pageindex, 10, this.searchStr);
    }
  }
}
</script>

